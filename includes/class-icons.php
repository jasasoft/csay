     * Render an icon. Returns the SVG markup.
     *
     * @param string $name   Icon name (e.g. 'message-circle')
     * @param int    $size   Icon size in pixels (default 16)
     * @param string $class  Extra CSS class(es) to add
     * @return string        Inline SVG markup, or empty string if not found
     */
    public static function render(string $name, int $size = 16, string $class = ''): string {
        $paths = self::$icons[$name] ?? null;
        if ($paths === null) {
            return '';
        }

        $classes = trim('cs-icon cs-icon-' . $name . ' ' . $class);

        return sprintf(
            '<svg class="%s" xmlns="http://www.w3.org/2000/svg" width="%d" height="%d" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" aria-hidden="true">%s</svg>',
            esc_attr($classes),
            $size,
            $size,
            $paths
        );
    }

    /**
     * Echo an icon directly.
     */
    public static function print(string $name, int $size = 16, string $class = ''): void {
        echo self::render($name, $size, $class);
    }

    /**
     * Check if an icon exists in the set.
     */
    public static function exists(string $name): bool {
        return isset(self::$icons[$name]);
    }

